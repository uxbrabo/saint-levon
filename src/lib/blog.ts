export function getPostBySlug(slug: string) {
  return posts.find((p) => p.slug === slug);
}

export function getRelatedPosts(currentSlug: string, categoria: string, limit = 2) {
  return posts.filter((p) => p.slug !== currentSlug && p.categoria === categoria).slice(0, limit);
}
